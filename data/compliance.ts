export function getComplianceBySubmissionId(submissionId: string): ComplianceResult | undefined {
  return complianceResults.find(c => c.submissionId === submissionId)
}

export function getComplianceStats() {
  const total = complianceResults.length
  const passed = complianceResults.filter(c => c.status === 'passed').length
  const failed = complianceResults.filter(c => c.status === 'failed').length
  const inProgress = complianceResults.filter(c => c.status === 'in_progress').length
  const conditional = complianceResults.filter(c => c.status === 'conditional').length
  return { total, passed, failed, inProgress, conditional }
}
