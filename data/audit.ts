export function getAuditLogsByEntity(entityId: string): AuditLog[] {
  return auditLogs.filter(a => a.entityId === entityId)
}

export function getRecentAuditLogs(limit = 10): AuditLog[] {
  return [...auditLogs]
    .sort((a, b) => new Date(b.timestamp).getTime() - new Date(a.timestamp).getTime())
    .slice(0, limit)
}

export function getAuditStats() {
  const total = auditLogs.length
  const today = new Date().toDateString()
  const todayCount = auditLogs.filter(a => new Date(a.timestamp).toDateString() === today).length
  const byRole = auditLogs.reduce<Record<string, number>>((acc, a) => {
    acc[a.actorRole] = (acc[a.actorRole] ?? 0) + 1
    return acc
  }, {})
  return { total, todayCount, byRole }
}
