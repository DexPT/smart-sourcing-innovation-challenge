export function getSubmissionById(id: string): Submission | undefined {
  return submissions.find(s => s.id === id)
}

export function getSubmissionsByStatus(status: string): Submission[] {
  return submissions.filter(s => s.status === status)
}

export function getSubmissionStats() {
  const total = submissions.length
  const byStatus = submissions.reduce<Record<string, number>>((acc, s) => {
    acc[s.status] = (acc[s.status] ?? 0) + 1
    return acc
  }, {})
  const avgScore = submissions.filter(s => s.aiScore).reduce((sum, s) => sum + (s.aiScore?.overall ?? 0), 0) / submissions.filter(s => s.aiScore).length
  const totalValue = submissions.filter(s => s.status !== 'rejected').reduce((sum, s) => sum + s.estimatedValue, 0)
  return { total, byStatus, avgScore: Math.round(avgScore), totalValue }
}
